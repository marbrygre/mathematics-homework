#include <iostream>
#include <cmath>
using namespace std;
int main() {
    // Prompt user to input two numbers
    cout << "Enter two numbers: ";
    int num1, num2;
    cin >> num1 >> num2;
    
    // Calculate the sum of the two numbers
    int sum = num1 + num2;
    
    // Display the sum
    cout << "The sum is: " << sum << endl;
    
    return 0;
}
